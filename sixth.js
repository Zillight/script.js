// Strings

const age = 20;
const text = `I am ${age}years old`;

console.log(text); // I am 20years old
console.log(typeof(text)); // string

// Using 'bracket notation' to call out the characters in the string 
console.log(text[0]); // I
console.log(text[1]); // space
console.log(text[2]); // a
console.log(text[3]); // m
console.log(text[4]); // space
console.log(text[5]); // 2
console.log(text[6]); // 0