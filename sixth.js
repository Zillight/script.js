// Objects in JS
// Accessing Object properties using Dot Notation

const person = {
    name:'Ara',     // Object properties
    age: 2
}

console.log(person); // Prints 2
console.log(person.name); // Prints Ara
console.log(person.age); // Prints 2
person.age = 7;
console.log(person); // Prints 2
console.log(person.age); // Prints 2