// Strings

const age = 20;
const text = `I am ${age}years old`;

console.log(text); // I am 20years old
console.log(typeof(text)); // string

// Using charAt

console.log(text.charAt(0)); // I
console.log(text.charAt(1)); // space
console.log(text.charAt(2)); // a
console.log(text.charAt(3)); // m
console.log(text.charAt(4)); // space
console.log(text.charAt(5)); // 2
console.log(text.charAt(6)); // 0