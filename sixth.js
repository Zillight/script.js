// Strings Methods
// 1. toUpperCase() and toLowerCase() Method

const text = `I love Jesus`
const result = text.toUpperCase(); // Note: Do not forget to type the U and C in caps
console.log(result); // I LOVE JESUS

const maxim = `MONEY is not EVERYTHING`;
console.log(maxim.toLowerCase()); // money is not everything
// Dont forget to type the L and C a-in caps and add ().